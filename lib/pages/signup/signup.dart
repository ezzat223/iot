  void signUp(String email, String password, String username) async {
    if (_formkey.currentState!.validate()) {
      bool isUsernameAvailable = await checkUsernameAvailability(username);
      if (!isUsernameAvailable) {
        showErrorMessage("Username is already taken. Please choose another.");
        return;
      }

      bool isEmailAvailable = await checkEmailAvailability(email);
      if (!isEmailAvailable) {
        showErrorMessage("Email is already registered. Please use another.");
        return;
      }

      try {
        UserCredential userCredential = await _auth
            .createUserWithEmailAndPassword(email: email, password: password);

        if (userCredential.user != null) {
          postDetailsToFirestore(email, username); // Store username directly
          showSuccessMessage("Registration successful!");
          Navigator.pushReplacement(
              context, MaterialPageRoute(builder: (context) => LoginPage()));
        }
      } catch (e) {
        showErrorMessage("Registration failed. Please try again.");
        print("Error occurred during registration: $e");
      }
    }
  }

  Future<bool> checkUsernameAvailability(String username) async {
    QuerySnapshot querySnapshot = await FirebaseFirestore.instance
        .collection('users')
        .where('username',
            isEqualTo: username.toLowerCase()) // Check for lowercase username
        .get();

    return querySnapshot.docs.isEmpty;
  }

  Future<bool> checkEmailAvailability(String email) async {
    QuerySnapshot querySnapshot = await FirebaseFirestore.instance
        .collection('users')
        .where('email', isEqualTo: email)
        .get();

    return querySnapshot.docs.isEmpty;
  }

  void postDetailsToFirestore(String email, String username) async {
    FirebaseFirestore firebaseFirestore = FirebaseFirestore.instance;
    var user = _auth.currentUser;
    CollectionReference ref = FirebaseFirestore.instance.collection('users');
    ref.doc(user!.uid).set({
      'email': emailController.text,
      'username': username,
      'role': 'Doctor'
    });
  }

  void showSuccessMessage(String message) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Text(message),
        backgroundColor: Colors.green,
      ),
    );
  }

  void showErrorMessage(String message) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Text(message),
        backgroundColor: Colors.red,
      ),
    );
  }
}
